                c3[14] += (x3 & (1u << 14)) ? a : -a; c3[15] += (x3 & (1u << 15)) ? a : -a;
                c3[16] += (x3 & (1u << 16)) ? a : -a; c3[17] += (x3 & (1u << 17)) ? a : -a;
                c3[18] += (x3 & (1u << 18)) ? a : -a; c3[19] += (x3 & (1u << 19)) ? a : -a;
                c3[20] += (x3 & (1u << 20)) ? a : -a; c3[21] += (x3 & (1u << 21)) ? a : -a;
