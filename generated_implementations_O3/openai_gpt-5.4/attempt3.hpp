                c1[12] += (x1 & (1u << 12)) ? a : -a; c1[13] += (x1 & (1u << 13)) ? a : -a;
                c1[14] += (x1 & (1u << 14)) ? a : -a; c1[15] += (x1 & (1u << 15)) ? a : -a;
